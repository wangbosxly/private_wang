    /**
     *  通过登录用户名遍历权限密匙，并验证绑定角色。
     * @param userName 登录用户名
     * @return User 绑定了接口信息的user
     */
    @Transactional(readOnly = false)
	public User initUser(String userName){
        User user = getUserByLoginName(userName);
        if(user==null){
            return null;
        }
        Md5PwdEncoder pwdEncoder = new Md5PwdEncoder();
        List<Token> tokens = this.getTokens(user);
        //清除用户的权限，根据密匙重新添加
        userDao.deleteUserRole(user);
        //添加个人权限
        Role role = new Role();
        role.setEnname("personalRole");
        role = roleDao.getByEnname(role);
        List<Role> list = new ArrayList<Role>();
        list.add(role);
        //循环遍历验证密匙
        for (Token token : tokens) {
            //根据token获取不同类型的tokenHandler
            TokenHandler tokenHandler = TokenHandleFactory.build(token);
            String state = null;
            try {
                if(tokenHandler!=null){
                    //从执行器中获取密匙验证信息
                    JSONObject jsonObject = tokenHandler.validateInfo();
                    Role tokenRole = roleDao.get(token.getRoleId());
                    //判断返回值是否有信息
                    if (PostHelperUtils.hasInfo(jsonObject)) {
                        //给user中绑定返回信息
                        tokenHandler.prepareUser(user);
                      //  getUserInfo(user, jsonObject);
                        //通过执行器获取密匙状态
                        state = tokenHandler.getState();
                        //如果状态正常，则添加相应角色
                        if ("0".equals(state)) {
                            list.add(tokenRole);
                        }
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            if(state==null){
                //token没有通过验证，证明失效
                state = "1";
            }
            //如果token的状态发生变化则更新token的状态
            if (!token.getState().equals(state)) {
                token.setState(state);
                tokenDao.update(token);
            }
        }
        user.setRoleList(list);
        userDao.insertUserRole(user);
        return user;
    }

